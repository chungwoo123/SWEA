'''
당신은 무한한 2차원 좌표평면 위의 원점 (0, 0)에 서 있다.

당신은 크기를 무시할 수 있는 아주 작은 점으로 생각할 수 있다.

 

당신은 앞으로 좌표평면 위를 뛰어다닐 것이다.

당신은 두 점 사이를 이동할 때마다 제자리멀리뛰기를 해야 하는데, 이를 통해 이동할 수 있는 거리 d0, d1, ⋯, dN-1 이 이미 정해져 있다.

모든 음이 아닌 정수 i에 대해, i+1번째 점프의 거리는 무조건 di mod N이어야 (즉, 출발 점과 도착 점 사이의 직선 거리가 di mod N이어야) 한다.

 

예를 들어 d = [2, 3, 7]이라면, 당신의 점프 거리는 2, 3, 7, 2, 3, 7, 2, 3, 7, ⋯, 순서대로 반복된다.

거리 조건만 만족한다면 좌표 평면 위의 어떤 점이라도 갈 수 있으며, 좌표가 정수일 필요가 없다.

 

당신은 이 규칙을 지키며 점프를 하여 좌표평면 위의 점 (X,0)에 방문하고자 한다.

이것이 가능한지 판단하고, 가능하다면 이를 위해 필요한 최소 점프 횟수를 구하는 프로그램을 작성하라.

 

[입력]
 

첫 번째 줄에 테스트 케이스의 수 T가 주어진다.

각 테스트 케이스는 두 개의 줄로 구성된다.

첫 번째 줄에는 두 개의 정수 N (1 ≤ N ≤ 50) 과 X (-109 ≤ X ≤ 109) 가 주어진다.

두 번째 줄에는 N개의 자연수 d0, d1, ⋯, dN-1 (1 ≤ di ≤ 109) 가 공백 하나씩을 사이로 두고 주어진다.


 

[출력]
 

각 테스트 케이스마다,

    -  이 규칙을 지키며 점프를 하여 좌표평면 위의 점 (X,0)에 방문할 수 있다면, 필요한 최소 점프 횟수를 출력한다.

    -  방문할 수 없다면, -1을 출력한다.

기본 제공코드는 임의 수정해도 관계 없습니다. 단, 입출력 포맷 주의

'''

