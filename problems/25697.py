'''
아래의 구문은 input.txt 를 read only 형식으로 연 후,
앞으로 표준 입력(키보드) 대신 input.txt 파일로부터 읽어오겠다는 의미의 코드입니다.
여러분이 작성한 코드를 테스트 할 때, 편의를 위해서 input.txt에 입력을 저장한 후,
아래 구문을 이용하면 이후 입력을 수행할 때 표준 입력 대신 파일로부터 입력을 받아올 수 있습니다.
따라서 테스트를 수행할 때에는 아래 주석을 지우고 이 구문을 사용하셔도 좋습니다.
아래 구문을 사용하기 위해서는 import sys가 필요합니다.
단, 채점을 위해 코드를 제출하실 때에는 반드시 아래 구문을 지우거나 주석 처리 하셔야 합니다.
'''
import sys
sys.stdin = open("input.txt", "r")

T = int(input())
for test_case in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    N, X = map(int, input().split())
    D = list(map(int, input().split()))

    total_dist = sum(D)

    # Case 1: X가 합보다 크면 절대 불가능
    if X > total_dist:
        print("#{}".format(test_case, -1))
        continue

    # Case 2: X와 합이 모두 홀수이거나 모두 짝수여야 함
    if (X % 2) != (total_dist % 2):
        print("#{}".format(test_case, -1))
        continue
    
    # Case 3: N=1인 경우
    if N == 1:
        if D[0] == X:
            print("#{}".format(test_case, 1))
        else:
            print("#{}".format(test_case, -1))
        continue

    # Case 4: 일반적인 경우
    # min_jumps[k] = k걸음까지 이동했을 때 도달 가능한 x좌표의 최솟값
    # max_jumps[k] = k걸음까지 이동했을 때 도달 가능한 x좌표의 최댓값
    # (주의: 점프 순서가 고정되어 있으므로, 사실상 단순히 부분합의 범위만 추적하면 됨)
    
    min_jumps = [0] * (N + 1)
    max_jumps = [0] * (N + 1)
    
    for i in range(N):
        min_jumps[i+1] = min_jumps[i] + D[i]
        max_jumps[i+1] = max_jumps[i] + D[i]

    # X가 최소 1걸음부터 N걸음 범위 안에 있는지 확인
    possible = False
    for k in range(1, N + 1):
        current_dist = sum(D[:k])
        if min_jumps[N] - current_dist <= X <= max_jumps[N] - current_dist:
            # 이 경우 최소 걸음 수는 k가 됨
            possible = True
            break
            
    if possible:
        print("#{}".format(test_case, k))
    else:
        print("#{}".format(test_case, -1))
    # //////////////////////////////////////////////////////////////////////////////////